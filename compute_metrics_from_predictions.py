#!/usr/bin/env python3
"""
Utility to compute comprehensive metrics from saved prediction artifacts.

This avoids rebuilding a model with potentially mismatched architecture / scaler.
It consumes prediction files generated by the main training pipeline and produces
JSON metrics fully aligned with the reported test-set predictions.
"""

import argparse
import json
from pathlib import Path

import numpy as np

from compute_complete_metrics import MetricsComputer


def compute_metrics_from_predictions(predictions_path: str, output_path: str, num_classes: int = 38, model_name: str = "MLP-Full"):
    data = np.load(predictions_path, allow_pickle=True)
    y_true = data["y_true"]
    y_pred = data["y_pred"]
    y_proba = data["y_proba"]

    computer = MetricsComputer(num_classes=num_classes)
    computer.compute_all_metrics(
        y_true=y_true,
        y_pred_proba=y_proba,
        y_pred_labels=y_pred,
        model_name=model_name,
    )

    output_file = Path(output_path)
    output_file.parent.mkdir(parents=True, exist_ok=True)
    computer.save_metrics_json(str(output_file))
    return computer.metrics[model_name]


def main():
    parser = argparse.ArgumentParser(description="Compute aligned metrics from saved predictions")
    parser.add_argument("--predictions", required=True, help="Path to .npz predictions artifact")
    parser.add_argument("--output", required=True, help="Output JSON path")
    parser.add_argument("--num_classes", type=int, default=38)
    parser.add_argument("--model_name", default="MLP-Full")
    args = parser.parse_args()

    metrics = compute_metrics_from_predictions(
        predictions_path=args.predictions,
        output_path=args.output,
        num_classes=args.num_classes,
        model_name=args.model_name,
    )
    print(json.dumps({args.model_name: metrics}, indent=2))


if __name__ == "__main__":
    main()
